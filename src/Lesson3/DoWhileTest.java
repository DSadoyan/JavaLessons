package Lesson3;

public class DoWhileTest {
    public static void main(String[] args) {
        int x = 10;
        do {
            System.out.println("Hello");
            x++;
        } while (x < 12);
        System.out.println("---------------");
        int y = 5;
        do {
            System.out.println("Hi");
            y++;
        } while (y <= 7);
    }
}
